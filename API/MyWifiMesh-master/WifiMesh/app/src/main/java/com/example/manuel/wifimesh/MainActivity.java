public class MainActivity extends AppCompatActivity {


    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        final Connection conn = new Connection(this);

        Button button1 = (Button) findViewById(R.id.button1);
        button1.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                conn.connect("00:73:8d:fe:41:0d");
            }
        });
        button1.setText("Start Access Point");

        Button button2 = (Button) findViewById(R.id.button2);
        button2.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                conn.startServiceDiscovery();
            }
        });
        button2.setText("Start Service Discovery");

        Button button3 = (Button) findViewById(R.id.button3);
        button3.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                conn.send("Dine Muater".getBytes());
            }
        });
        button3.setText("Send");

        Button button4 = (Button) findViewById(R.id.button4);
        button4.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                conn.closeConnection();
            }
        });
        button4.setText("Close connection");


        Button btnPeers = (Button) findViewById(R.id.btnPeers);
        final TextView txtPeers = (TextView) findViewById(R.id.txtPeers);
        btnPeers.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {


                for(ScanResult results : conn.peers){

                    txtPeers.append(results.BSSID + "\n");

                }


            }
        });
    }
}
